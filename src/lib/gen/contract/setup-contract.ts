/**
 * Setup contract types and helpers.
 *
 * A setup contract captures the runtime integration/provider/env decisions
 * for a generated project.  It bridges the planner, pre-generation contract
 * inference, the integration panel, readiness gating, and deploy.
 *
 * The canonical source of truth for resolved env values is
 * `project_data.meta.projectEnvVars` via `src/lib/project-env-vars.ts`.
 * This module owns the *requirement* side, not the *value* side.
 */

import type { PlanContracts, PlanIntegrationContract } from "../plan/schema";

export type SetupStatus =
  | "unresolved"
  | "mocked"
  | "configured"
  | "publish-blocked";

export type SetupProviderChoice = {
  provider: string;
  name: string;
  status: "chosen" | "unresolved" | "optional" | "mocked";
  envVars: string[];
  setupGuide?: string;
};

export type SetupContract = {
  dataMode: PlanContracts["dataMode"];
  databaseProvider: SetupProviderChoice | null;
  authProvider: SetupProviderChoice | null;
  paymentProvider: SetupProviderChoice | null;
  integrations: SetupProviderChoice[];
  requiredEnvKeys: string[];
  status: SetupStatus;
};

function providerFromPlanIntegration(
  integration: PlanIntegrationContract,
): SetupProviderChoice {
  return {
    provider: integration.provider,
    name: integration.name,
    status: integration.status === "chosen"
      ? "chosen"
      : integration.status === "optional"
        ? "optional"
        : "unresolved",
    envVars: integration.envVars ?? [],
  };
}

function providerFromString(
  value: string | undefined,
  kind: "database" | "auth" | "payment",
  planIntegrations: PlanIntegrationContract[],
): SetupProviderChoice | null {
  if (!value || value === "ingen" || value === "none") return null;
  if (value === "unresolved" || value === "unknown") {
    return { provider: value, name: value, status: "unresolved", envVars: [] };
  }
  if (value === "mock data" || value === "mocked") {
    return { provider: "mock", name: "Mock data", status: "mocked", envVars: [] };
  }
  const match = planIntegrations.find(
    (i) => i.provider.toLowerCase() === value.toLowerCase(),
  );
  if (match) return providerFromPlanIntegration(match);
  return { provider: value, name: value, status: "chosen", envVars: [] };
}

export function deriveSetupContract(
  contracts: PlanContracts | undefined | null,
  configuredEnvKeys: Set<string>,
): SetupContract {
  const integrations = (contracts?.integrations ?? []).map(providerFromPlanIntegration);
  const planIntegrations = contracts?.integrations ?? [];

  const databaseProvider = providerFromString(
    contracts?.databaseProvider,
    "database",
    planIntegrations,
  );
  const authProvider = providerFromString(
    contracts?.authProvider,
    "auth",
    planIntegrations,
  );
  const paymentProvider = providerFromString(
    contracts?.paymentProvider,
    "payment",
    planIntegrations,
  );

  const allEnvKeys = new Set<string>();
  for (const env of contracts?.envVars ?? []) {
    if (env.key) allEnvKeys.add(env.key);
  }
  [databaseProvider, authProvider, paymentProvider, ...integrations].forEach(
    (choice) => {
      if (!choice) return;
      for (const key of choice.envVars) {
        allEnvKeys.add(key);
      }
    },
  );

  const requiredEnvKeys = Array.from(allEnvKeys).sort();
  const missingEnvKeys = requiredEnvKeys.filter(
    (key) => !configuredEnvKeys.has(key),
  );

  const hasUnresolved =
    databaseProvider?.status === "unresolved" ||
    authProvider?.status === "unresolved" ||
    paymentProvider?.status === "unresolved" ||
    integrations.some((i) => i.status === "unresolved");

  const dataMode = contracts?.dataMode ?? "none";

  let status: SetupStatus;
  if (hasUnresolved) {
    status = "unresolved";
  } else if (missingEnvKeys.length > 0) {
    status = "publish-blocked";
  } else if (dataMode === "mocked") {
    status = "mocked";
  } else {
    status = "configured";
  }

  return {
    dataMode,
    databaseProvider,
    authProvider,
    paymentProvider,
    integrations,
    requiredEnvKeys,
    status,
  };
}

export function buildEnvExampleContent(
  setup: SetupContract,
): string {
  const lines: string[] = [
    "# Environment variables required by this project",
    "# Generated by Sajtmaskin builder — fill in real values before publishing.",
    "",
  ];

  const grouped = new Map<string, string[]>();
  function addToGroup(group: string, keys: string[]) {
    const existing = grouped.get(group) ?? [];
    for (const key of keys) {
      if (!existing.includes(key)) existing.push(key);
    }
    grouped.set(group, existing);
  }

  if (setup.databaseProvider && setup.databaseProvider.envVars.length > 0) {
    addToGroup(`Database (${setup.databaseProvider.name})`, setup.databaseProvider.envVars);
  }
  if (setup.authProvider && setup.authProvider.envVars.length > 0) {
    addToGroup(`Auth (${setup.authProvider.name})`, setup.authProvider.envVars);
  }
  if (setup.paymentProvider && setup.paymentProvider.envVars.length > 0) {
    addToGroup(`Payment (${setup.paymentProvider.name})`, setup.paymentProvider.envVars);
  }
  for (const integration of setup.integrations) {
    if (integration.envVars.length > 0) {
      addToGroup(integration.name, integration.envVars);
    }
  }

  const ungroupedKeys = setup.requiredEnvKeys.filter((key) => {
    for (const keys of grouped.values()) {
      if (keys.includes(key)) return false;
    }
    return true;
  });
  if (ungroupedKeys.length > 0) {
    addToGroup("Other", ungroupedKeys);
  }

  for (const [group, keys] of grouped) {
    lines.push(`# ${group}`);
    for (const key of keys) {
      lines.push(`${key}=`);
    }
    lines.push("");
  }

  return lines.join("\n");
}
