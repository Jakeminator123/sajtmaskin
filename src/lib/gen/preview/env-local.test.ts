import { describe, expect, it, vi } from "vitest";

vi.mock("@/lib/project-env-vars", () => ({
  getStoredProjectEnvVarMap: vi.fn(async () => ({})),
}));

import {
  buildPreviewEnvLocalContents,
  formatDotenvBody,
  mergePreviewEnvRecords,
  resolvePreviewEnvLayers,
} from "./env-local";

describe("mergePreviewEnvRecords", () => {
  it("later layers override earlier keys", () => {
    const merged = mergePreviewEnvRecords(
      { A: "1", B: "from-placeholder" },
      { B: "from-project", C: "3" },
      { C: "from-generated" },
    );
    expect(merged).toEqual({
      A: "1",
      B: "from-project",
      C: "from-generated",
    });
  });

  it("supports any number of layers", () => {
    const merged = mergePreviewEnvRecords(
      { A: "harmless" },
      { B: "tier3-stub" },
      { C: "project-preview" },
      { B: "user-override" },
      { C: "generated-override" },
    );
    expect(merged).toEqual({
      A: "harmless",
      B: "user-override",
      C: "generated-override",
    });
  });
});

describe("buildPreviewEnvLocalContents", () => {
  it("includes placeholder keys and applies project map when mocked", async () => {
    const { getStoredProjectEnvVarMap } = await import("@/lib/project-env-vars");
    vi.mocked(getStoredProjectEnvVarMap).mockResolvedValueOnce({
      STRIPE_SECRET_KEY: "sk_from_project",
    });
    const body = await buildPreviewEnvLocalContents({
      appProjectId: "proj_test",
      generatedEnvLocal: "FOO=from_gen\nSTRIPE_SECRET_KEY=sk_from_model",
    });
    expect(body).toContain("FOO=from_gen");
    expect(body).toContain("STRIPE_SECRET_KEY=sk_from_model");
    expect(body).not.toContain("sk_from_project");
    expect(body).toContain("NEXT_PUBLIC_SAJTMASKIN_PROJECT_ID=proj_test");
    expect(body).toContain("SAJTMASKIN_APP_PROJECT_ID=proj_test");
  });

  it("groups output by provenance with section comments", async () => {
    const { getStoredProjectEnvVarMap } = await import("@/lib/project-env-vars");
    vi.mocked(getStoredProjectEnvVarMap).mockResolvedValueOnce({
      MY_REAL_KEY: "real_value",
    });
    const body = await buildPreviewEnvLocalContents({
      appProjectId: "proj_test",
      generatedEnvLocal: "GEN_KEY=gen_value",
    });
    expect(body).toContain("# ── Your configured values");
    expect(body).toContain("MY_REAL_KEY=real_value");
    expect(body).toContain("# ── Generated by the model");
    expect(body).toContain("GEN_KEY=gen_value");
    expect(body).toContain("# ── Harmless placeholders");
    expect(body).toContain("# ── Tier-3 stub placeholders");
    expect(body).toContain("# ── Project preview tokens (auto)");
  });

  it("strips tier-3 stub layer for F3 (lifecycleStage='integrations')", async () => {
    const { getStoredProjectEnvVarMap } = await import("@/lib/project-env-vars");
    vi.mocked(getStoredProjectEnvVarMap).mockResolvedValueOnce({});
    const body = await buildPreviewEnvLocalContents({
      appProjectId: "proj_test",
      generatedEnvLocal: null,
      lifecycleStage: "integrations",
    });
    // Harmless layer must remain
    expect(body).toContain("NEXT_PUBLIC_STRIPE_PUBLISHABLE_KEY=");
    // Tier-3 stub layer must be stripped (Stripe secret is in tier-3 stub file)
    expect(body).not.toContain("STRIPE_SECRET_KEY=");
    expect(body).not.toContain("# ── Tier-3 stub placeholders");
  });
});

describe("dotenv round-trip (parseDotenvBody ↔ quoteEnvValue)", () => {
  it("does NOT inflate escape levels across one round-trip", async () => {
    const { getStoredProjectEnvVarMap } = await import("@/lib/project-env-vars");

    // A value with all the chars that quoteEnvValue escapes:
    // backslash, double-quote, newline, plus a regular space (forces quoting).
    const original = 'multi\nline "quoted" \\ slash';
    const encodedOnce = formatDotenvBody({ KEY: original });

    // Feed the encoded form back as the model-emitted .env.local.
    // resolvePreviewEnvLayers → parseDotenvBody must decode symmetrically.
    vi.mocked(getStoredProjectEnvVarMap).mockResolvedValueOnce({});
    const { merged } = await resolvePreviewEnvLayers({
      appProjectId: "proj_rt",
      generatedEnvLocal: encodedOnce,
    });

    expect(merged.KEY).toBe(original);

    // Re-encode the merged result and verify it equals the first encoding —
    // i.e. the cycle is idempotent and escapes don't double up.
    const encodedTwice = formatDotenvBody({ KEY: merged.KEY ?? "" });
    expect(encodedTwice).toBe(encodedOnce);
  });

  it("leaves single-quoted values literal (POSIX shell semantics)", async () => {
    const { getStoredProjectEnvVarMap } = await import("@/lib/project-env-vars");
    vi.mocked(getStoredProjectEnvVarMap).mockResolvedValueOnce({});
    const { merged } = await resolvePreviewEnvLayers({
      appProjectId: "proj_rt",
      generatedEnvLocal: "KEY='no\\nescape'",
    });
    expect(merged.KEY).toBe("no\\nescape");
  });
});

describe("resolvePreviewEnvLayers", () => {
  it("tracks provenance per key with later layers winning", async () => {
    const { getStoredProjectEnvVarMap } = await import("@/lib/project-env-vars");
    vi.mocked(getStoredProjectEnvVarMap).mockResolvedValueOnce({
      STRIPE_SECRET_KEY: "sk_real",
    });
    const { merged, provenance } = await resolvePreviewEnvLayers({
      appProjectId: "proj_test",
      generatedEnvLocal: "FOO=bar",
    });
    expect(merged.STRIPE_SECRET_KEY).toBe("sk_real");
    expect(provenance.STRIPE_SECRET_KEY).toBe("user");
    expect(merged.FOO).toBe("bar");
    expect(provenance.FOO).toBe("generated");
    expect(provenance.RESEND_API_KEY).toBe("tier3-stub");
    expect(provenance.NEXT_PUBLIC_STRIPE_PUBLISHABLE_KEY).toBe("harmless");
    expect(provenance.NEXT_PUBLIC_SAJTMASKIN_PROJECT_ID).toBe("project-preview");
  });

  it("omits tier-3 stub provenance when lifecycleStage='integrations'", async () => {
    const { getStoredProjectEnvVarMap } = await import("@/lib/project-env-vars");
    vi.mocked(getStoredProjectEnvVarMap).mockResolvedValueOnce({});
    const { merged, provenance } = await resolvePreviewEnvLayers({
      appProjectId: "proj_test",
      generatedEnvLocal: null,
      lifecycleStage: "integrations",
    });
    expect(merged.STRIPE_SECRET_KEY).toBeUndefined();
    expect(provenance.STRIPE_SECRET_KEY).toBeUndefined();
    expect(provenance.NEXT_PUBLIC_STRIPE_PUBLISHABLE_KEY).toBe("harmless");
  });
});
