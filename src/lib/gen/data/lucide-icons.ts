/**
 * Set of valid Lucide icon names (PascalCase, as exported by lucide-react).
 *
 * Generated from lucide-react 0.563.0 runtime exports — the version pinned for
 * GENERATED PROJECTS in `src/lib/gen/export/project-scaffold.ts`, kept equal to
 * this repo's `lucide-react` dependency so the allowlist, the import validator,
 * and the shipped runtime all agree (enforced by the project-scaffold baseline
 * parity test + `scripts/dev/check-lucide-icons.mjs`).
 *
 * Policy: includes the two identifier forms shadcn/ui and the LLM actually emit
 * — the base name (`Camera`) and the `*Icon` alias (`CameraIcon`). Legacy
 * `Lucide*` prefix aliases and the generic `Icon` export are excluded. The set
 * stays broad enough that valid icons such as `Cpu` are preserved instead of
 * being routed through fuzzy fallback logic.
 *
 * Brand/semantic aliases live in LUCIDE_BRAND_ICON_REPLACEMENTS below and
 * intentionally win before exact runtime export matches.
 *
 * To update: bump the generated-project `lucide-react` pin to match this repo's
 * version, then run `node scripts/dev/generate-lucide-icons.mjs`.
 */

