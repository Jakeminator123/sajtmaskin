export type BuildSpecGenerationMode = "init" | "followUp";

/**
 * `init` semantics: first real codegen in this chat. A follow-up turn that
 * is the first code generation after a contract gate is treated as init too.
 *
 * Single canonical helper — previously inlined in `orchestrate.ts` (×2),
 * `build-spec.ts` and `stream/finalize-preflight.ts`. Keep them aligned by
 * importing this from a single place.
 */
function isEffectiveInit(params: {
  generationMode: BuildSpecGenerationMode | "init" | "followUp" | undefined;
  isFirstCodeGeneration?: boolean | null;
}): boolean {
  const mode = params.generationMode;
  return (
    mode === "init" ||
    (mode === "followUp" && Boolean(params.isFirstCodeGeneration))
  );
}
export type BuildSpecChangeScope =
  | "copy"
  | "local-layout"
  | "page-addition"
  | "redesign"
  | "integration";
export type BuildSpecQualityTarget = "standard" | "premium" | "release-candidate";
export type BuildSpecPreviewPolicy = "fidelity2" | "fidelity3";
export type BuildSpecVerificationPolicy = "fast" | "standard" | "strict";
export type BuildSpecContextPolicy = "light" | "normal" | "heavy";

/**
 * Token fields drive runtime budgets (`systemContextTokens`, etc.).
 * `*Chars` fields are approximate compat mirrors via `estimateCharsForTokens` for
 * char-oriented call-sites (e.g. scaffold serialization), not a second source of truth.
 */
export interface BuildSpecTokenBudgets {
  scaffoldTokens?: number;
  refsTokens?: number;
  systemContextTokens?: number;
  scaffoldChars: number;
  refsChars: number;
  systemContextChars: number;
}

export interface RouteRealizationPolicy {
  mode: "full" | "primary-full-with-shells";
  primaryRoutePath: string;
  fullRoutePaths: string[];
  shellRoutePaths: string[];
}

/**
 * Fingerprint present in mechanically generated shell pages.
 * Used to detect whether an existing file is still a deferred shell
 * so follow-ups can preserve it unless the user explicitly asks to expand.
 */
const SHELL_PAGE_FINGERPRINT = "bg-[oklch(0.58_0.22_262)]";

/**
 * Detects whether file content is a deferred shell page generated by
 * `buildShellPageContent()`. Uses multiple signals so that minor autofix
 * reformats don't break detection.
 */
export function isShellPageContent(content: string): boolean {
  if (!content || content.length > 8_000) return false;
  const signals = [
    content.includes(SHELL_PAGE_FINGERPRINT),
    content.includes("Förberedd sida") || content.includes("Dynamisk routeskal"),
    content.includes("Skapa sida"),
    content.includes("Varför sidan är enkel just nu"),
  ];
  return signals.filter(Boolean).length >= 2;
}

export interface BuildSpec {
  buildIntent: BuildIntent;
  generationMode: BuildSpecGenerationMode;
  changeScope: BuildSpecChangeScope;
  scaffoldId: ScaffoldId | null;
  routePlanSummary: string;
  stylePack: string;
  qualityTarget: BuildSpecQualityTarget;
  previewPolicy: BuildSpecPreviewPolicy;
  verificationPolicy: BuildSpecVerificationPolicy;
  contextPolicy: BuildSpecContextPolicy;
  referenceCategories: string[];
  forbiddenPatterns: string[];
  tokenBudgets: BuildSpecTokenBudgets;
  routeRealization?: RouteRealizationPolicy;
}

type DeriveBuildSpecParams = {
  prompt: string;
  buildIntent: BuildIntent;
  generationMode: BuildSpecGenerationMode;
  resolvedScaffold: ScaffoldManifest | null;
  routePlan: RoutePlan;
  preGenerationContracts: PreGenerationContractContext;
  promptStrategyMeta?: Pick<PromptStrategyMeta, "strategy" | "promptType"> | null;
  capabilities?: InferredCapabilities | null;
  /**
   * True when this is the first real code generation in a chat that already
   * has a persistedScaffoldId (e.g. after a contract-gate turn).
   * Treated as effective-init for route realization deferral.
   */
  isFirstCodeGeneration?: boolean;
  /**
   * Route paths whose existing file content is a deferred shell page.
   * On follow-up, routes in this list that the user didn't explicitly
   * target are preserved as shells.
   */
  existingShellRoutePaths?: string[];
  /**
   * Explicit caller-controlled `previewPolicy` override.
   * F3 (`fidelity3`) is ONLY entered via this override — never auto-promoted
   * by prompt heuristics or buildIntent shape. Set by the F3 trigger route
   * (`POST /api/engine/chats/[chatId]/finalize-design`).
   */
  previewPolicyOverride?: BuildSpecPreviewPolicy;
};

function buildRoutePlanSummary(routePlan: RoutePlan): string {
  const routes = routePlan.routes
    .slice(0, 8)
    .map((route) => route.path)
    .join(",");
  return `${routePlan.provenance.primarySource}:${routePlan.siteType}:${routes || "/"}`;
}

function choosePrimaryRoutePath(params: {
  buildIntent: BuildIntent;
  routePlan: RoutePlan;
  prompt: string;
}): string {
  const { buildIntent, routePlan, prompt } = params;
  const normalizedPrompt = prompt.toLowerCase();
  const routePaths = routePlan.routes.map((route) => route.path);
  const rootRoute = routePlan.routes.find((route) => route.path === "/");
  const dashboardLikePath = routePlan.routes.find((route) =>
    ["/dashboard", "/app", "/workspace"].includes(route.path),
  )?.path;

  if (buildIntent === "app") {
    if (/\b(dashboard|instrumentpanel|workspace|app shell)\b/i.test(normalizedPrompt)) {
      return dashboardLikePath ?? rootRoute?.path ?? routePaths[0] ?? "/";
    }
    return rootRoute?.path ?? dashboardLikePath ?? routePaths[0] ?? "/";
  }

  if (rootRoute) return rootRoute.path;
  return routePaths[0] ?? "/";
}

const AUTH_ROUTE_PATHS = new Set([
  "/login",
  "/signup",
  "/forgot-password",
  "/reset-password",
]);

function deriveRequiredCompanionRoutes(params: {
  buildIntent: BuildIntent;
  prompt: string;
  primaryRoutePath: string;
  allRoutePaths: string[];
}): string[] {
  const { buildIntent, prompt, primaryRoutePath, allRoutePaths } = params;
  const authRoutes = allRoutePaths.filter((path) => AUTH_ROUTE_PATHS.has(path));
  const authSignals =
    buildIntent === "app" ||
    /\b(auth|login|sign.?in|signup|register|forgot.?password|reset.?password|lösenord|inlogg)\b/i.test(
      prompt,
    ) ||
    authRoutes.length >= 2;

  if (!authSignals || authRoutes.length === 0) return [];
  return authRoutes.filter((path) => path !== primaryRoutePath);
}

function deriveRouteRealizationPolicy(params: {
  generationMode: BuildSpecGenerationMode;
  buildIntent: BuildIntent;
  prompt: string;
  routePlan: RoutePlan;
  isFirstCodeGeneration?: boolean;
  existingShellRoutePaths?: string[];
}): RouteRealizationPolicy {
  const {
    generationMode,
    buildIntent,
    prompt,
    routePlan,
    isFirstCodeGeneration,
    existingShellRoutePaths = [],
  } = params;
  const allRoutePaths = routePlan.routes.map((route) => route.path);
  const primaryRoutePath = choosePrimaryRoutePath({ buildIntent, routePlan, prompt });

  const effectiveInit = isEffectiveInit({ generationMode, isFirstCodeGeneration });

  if (effectiveInit && FEATURES.deferExtraRoutesOnInit && allRoutePaths.length > 1) {
    const companionRoutePaths = deriveRequiredCompanionRoutes({
      buildIntent,
      prompt,
      primaryRoutePath,
      allRoutePaths,
    });
    const fullRoutePaths = Array.from(
      new Set([primaryRoutePath, ...companionRoutePaths]),
    );

    return {
      mode: "primary-full-with-shells",
      primaryRoutePath,
      fullRoutePaths,
      shellRoutePaths: allRoutePaths.filter((path) => !fullRoutePaths.includes(path)),
    };
  }

  if (
    !effectiveInit &&
    FEATURES.deferExtraRoutesOnInit &&
    existingShellRoutePaths.length > 0
  ) {
    const promptLower = prompt.toLowerCase();
    const shellsToPreserve = existingShellRoutePaths.filter((shellPath) => {
      const route = routePlan.routes.find(
        (r) => r.path === shellPath,
      );
      const routeName = route?.name?.toLowerCase() ?? "";
      const pathSegments = shellPath
        .split("/")
        .filter(Boolean)
        .map((s) => s.replace(/\[|\]/g, "").toLowerCase());

      const expandSignals = [
        "bygg ut",
        "bygg upp",
        "skapa sida",
        "expand",
        "fyll i",
        "fyll ut",
        "gör klar",
        "gör riktig",
        "gör komplett",
        "implementera",
        "utveckla",
        "lägg innehåll",
        "lägg till innehåll",
        "mer komplett",
        "more complete",
        "build out",
        "flesh out",
        "complete the",
        "finish the",
        "aktivera",
      ];
      const mentionsRoute =
        pathSegments.some((seg) => seg.length > 2 && promptLower.includes(seg)) ||
        (routeName.length > 2 && promptLower.includes(routeName));

      return !(mentionsRoute && expandSignals.some((sig) => promptLower.includes(sig)));
    });

    if (shellsToPreserve.length > 0) {
      return {
        mode: "primary-full-with-shells",
        primaryRoutePath,
        fullRoutePaths: allRoutePaths.filter(
          (path) => !shellsToPreserve.includes(path),
        ),
        shellRoutePaths: shellsToPreserve,
      };
    }
  }

  return {
    mode: "full",
    primaryRoutePath,
    fullRoutePaths: allRoutePaths,
    shellRoutePaths: [],
  };
}

function effectiveInitRouteCount(params: {
  generationMode: BuildSpecGenerationMode;
  routePlan: RoutePlan;
  routeRealization: RouteRealizationPolicy;
}): number {
  const { generationMode, routePlan, routeRealization } = params;
  if (
    generationMode === "init" &&
    routeRealization.mode === "primary-full-with-shells"
  ) {
    return routeRealization.fullRoutePaths.length;
  }
  return routePlan.routes.length;
}

function inferStylePack(
  prompt: string,
  buildIntent: BuildIntent,
  resolvedScaffold: ScaffoldManifest | null,
  changeScope: BuildSpecChangeScope,
): string {
  const promptLower = prompt.toLowerCase();
  if (/\bbrutalist\b/i.test(promptLower)) return "brutalist";
  if (/\beditorial\b/i.test(promptLower)) return "editorial";
  if (/\bminimal(?:ist)?\b/i.test(promptLower)) return "minimal";
  if (/\bluxury\b/i.test(promptLower)) return "luxury";
  if (/\bplayful\b/i.test(promptLower)) return "playful";
  if (/\bretro\b|\bvintage\b/i.test(promptLower)) return "retro";
  if (/\bfuturistic\b|\bcyberpunk\b/i.test(promptLower)) return "futuristic";
  if (resolvedScaffold?.id === "blog") return "editorial";
  if (resolvedScaffold?.id === "ecommerce") return "commerce";
  if (resolvedScaffold?.id === "saas-landing") return "saas";
  if (buildIntent === "app") return "app-product";
  if (changeScope === "copy") return "current-site";
  return "brand-led";
}

function inferChangeScope(params: {
  prompt: string;
  generationMode: BuildSpecGenerationMode;
  routePlan: RoutePlan;
  preGenerationContracts: PreGenerationContractContext;
}): BuildSpecChangeScope {
  const { prompt, generationMode, routePlan, preGenerationContracts } = params;
  const integrationCount = preGenerationContracts.contracts.integrations.length;
  const promptLower = prompt.toLowerCase();

  if (generationMode === "init") {
    if (
      integrationCount > 0 ||
      preGenerationContracts.contracts.dataMode === "persisted" ||
      Boolean(preGenerationContracts.contracts.databaseProvider) ||
      Boolean(preGenerationContracts.contracts.authProvider) ||
      Boolean(preGenerationContracts.contracts.paymentProvider)
    ) {
      return "integration";
    }
    if (routePlan.routes.length > 1) return "page-addition";
    return "redesign";
  }

  if (includesAny(REDESIGN_PATTERNS, promptLower)) return "redesign";
  if (
    integrationCount > 0 &&
    includesAny(INTEGRATION_PATTERNS, promptLower)
  ) {
    return "integration";
  }
  if (includesAny(PAGE_ADDITION_PATTERNS, promptLower)) return "page-addition";
  if (
    includesAny(COPY_PATTERNS, promptLower) &&
    (!includesAny(LAYOUT_PATTERNS, promptLower) || includesAny(COPY_GUARD_PATTERNS, promptLower))
  ) {
    return "copy";
  }
  if (includesAny(LAYOUT_PATTERNS, promptLower)) return "local-layout";
  return "local-layout";
}

function inferQualityTarget(params: {
  buildIntent: BuildIntent;
  generationMode: BuildSpecGenerationMode;
  resolvedScaffold: ScaffoldManifest | null;
  routePlan: RoutePlan;
  routeRealization: RouteRealizationPolicy;
  preGenerationContracts: PreGenerationContractContext;
  /** Explicit override — F3 lifecycle stage forces release-candidate. */
  previewPolicy: BuildSpecPreviewPolicy;
}): BuildSpecQualityTarget {
  const {
    buildIntent,
    generationMode,
    resolvedScaffold,
    routePlan,
    routeRealization,
    preGenerationContracts,
    previewPolicy,
  } = params;
  if (previewPolicy === "fidelity3") return "release-candidate";

  const advancedScaffoldId =
    resolvedScaffold?.id === "dashboard" ||
    resolvedScaffold?.id === "ecommerce" ||
    resolvedScaffold?.id === "app-shell" ||
    resolvedScaffold?.id === "saas-landing";
  const routeCount = effectiveInitRouteCount({
    generationMode,
    routePlan,
    routeRealization,
  });
  const premiumSignals =
    buildIntent === "app" ||
    routeCount > 4 ||
    routePlan.siteType === "content-heavy" ||
    (routePlan.provenance.primarySource === "scaffold" && routeCount >= 3) ||
    preGenerationContracts.contracts.integrations.length > 0 ||
    preGenerationContracts.contracts.dataMode === "persisted" ||
    advancedScaffoldId;

  // Multi-page renders that don't otherwise trip a premium signal still
  // benefit from the higher token / verification budget — single-route
  // landings have one chance to look right, but every additional route
  // multiplies the surface area where a "standard" budget can drop visual
  // polish or cross-route consistency. Promote when we're rendering more
  // than one route at init.
  if (!premiumSignals && routeCount > 1) {
    console.info("[build-spec] quality_target_promoted_for_multipage", {
      routeCount,
      buildIntent,
      generationMode,
      scaffoldId: resolvedScaffold?.id ?? null,
      from: "standard",
      to: "premium",
    });
    return "premium";
  }

  return premiumSignals ? "premium" : "standard";
}

/**
 * Default `previewPolicy`. **Always returns `fidelity2`.** F3 is opt-in via
 * `DeriveBuildSpecParams.previewPolicyOverride`, set explicitly by the
 * "Bygg integrationer" button (`POST .../finalize-design`). Auto-promotion
 * by prompt content or buildIntent shape was removed 2026-04 because it
 * caused unwanted F3 builds whenever a prompt happened to mention "deploy"
 * or "production".
 */
function inferPreviewPolicy(): BuildSpecPreviewPolicy {
  return "fidelity2";
}

function inferVerificationPolicy(params: {
  generationMode: BuildSpecGenerationMode;
  changeScope: BuildSpecChangeScope;
  previewPolicy: BuildSpecPreviewPolicy;
  capabilityHeavy: boolean;
}): BuildSpecVerificationPolicy {
  const { generationMode, changeScope, previewPolicy, capabilityHeavy } = params;
  if (previewPolicy === "fidelity3") return "strict";
  if (generationMode === "followUp" && capabilityHeavy) return "standard";
  if (generationMode === "followUp" && changeScope === "copy") {
    return "fast";
  }
  return "standard";
}

function isExplicitSmallFollowUpPrompt(prompt: string): boolean {
  const trimmedPrompt = prompt.trim();
  if (trimmedPrompt.length > 220) return false;
  if (includesAny(IMAGE_FOLLOWUP_ESCAPE_PATTERNS, trimmedPrompt)) return false;
  return (
    includesAny(SMALL_FOLLOW_UP_HINT_PATTERNS, trimmedPrompt) &&
    includesAny(SMALL_FOLLOW_UP_TARGET_PATTERNS, trimmedPrompt)
  );
}

export function deriveFollowUpContextPolicy(params: {
  prompt: string;
  skipIntentClassification?: boolean;
  followUpIntent?: "clear-redesign" | "clear-refine" | "ambiguous-redesign" | "ambiguous-followup" | "neutral";
  capabilityHeavy: boolean;
}): BuildSpecContextPolicy {
  const {
    prompt,
    skipIntentClassification = false,
    followUpIntent = "neutral",
    capabilityHeavy,
  } = params;
  if (skipIntentClassification) return "normal";
  if (followUpIntent === "clear-redesign") return "normal";
  if (capabilityHeavy) return "normal";
  if (looksDesignHeavyMessage(prompt)) return "normal";
  if (isExplicitSmallFollowUpPrompt(prompt)) return "light";
  return "normal";
}

function inferContextPolicy(params: {
  prompt: string;
  generationMode: BuildSpecGenerationMode;
  changeScope: BuildSpecChangeScope;
  buildIntent: BuildIntent;
  routePlan: RoutePlan;
  routeRealization: RouteRealizationPolicy;
  preGenerationContracts: PreGenerationContractContext;
  promptStrategyMeta?: Pick<PromptStrategyMeta, "strategy" | "promptType"> | null;
  capabilityHeavy: boolean;
}): BuildSpecContextPolicy {
  const {
    prompt,
    generationMode,
    changeScope,
    buildIntent,
    routePlan,
    routeRealization,
    preGenerationContracts,
    promptStrategyMeta,
    capabilityHeavy,
  } = params;
  if (generationMode === "followUp" && (changeScope === "copy" || changeScope === "local-layout")) {
    if (includesAny(TARGETED_REPAIR_PATTERNS, prompt)) {
      return "normal";
    }
    return deriveFollowUpContextPolicy({
      prompt,
      capabilityHeavy,
    });
  }
  const routePlanHeavyStructure =
    routePlan.siteType === "app-shell" ||
    (routePlan.siteType === "content-heavy" &&
      effectiveInitRouteCount({
        generationMode,
        routePlan,
        routeRealization,
      }) > 1) ||
    (routePlan.provenance.primarySource === "scaffold" &&
      effectiveInitRouteCount({
        generationMode,
        routePlan,
        routeRealization,
      }) >= 3);

  const routeCount = effectiveInitRouteCount({
    generationMode,
    routePlan,
    routeRealization,
  });

  if (
    promptStrategyMeta?.strategy === "phase_plan_build_refine" ||
    promptStrategyMeta?.strategy === "preserved" ||
    buildIntent === "app" ||
    routeCount > 4 ||
    routePlanHeavyStructure ||
    preGenerationContracts.contracts.integrations.length > 0 ||
    preGenerationContracts.contracts.dataMode === "persisted"
  ) {
    return "heavy";
  }
  return "normal";
}

/**
 * Token budgets per `contextPolicy`. Tuned to be **generous but not absurd**:
 * the smart `inferContextPolicy` already picks the right tier based on
 * change scope, route count, integrations, and capability heaviness — so the
 * absolute numbers just need enough headroom that block pruning rarely fires
 * unless we're genuinely overflowing.
 *
 * Rationale (as of 2026-04):
 * - Largest scaffold (ecommerce) is ~7.3k tokens fully serialized.
 * - Modern context windows (GPT-5, Claude 4) are 200k+; even `heavy` here
 *   sits at ~80k and leaves plenty for chat history + completion tokens.
 * - `*Chars` mirrors via `CHARS_PER_TOKEN_ESTIMATE = 3.2` for refs and
 *   system context; `scaffoldChars` is intentionally tighter (~1.9 ratio)
 *   so the scaffold block can't dominate the dynamic context.
 */
function tokenBudgetsForContextPolicy(contextPolicy: BuildSpecContextPolicy): BuildSpecTokenBudgets {
  switch (contextPolicy) {
    case "light":
      return {
        scaffoldTokens: 13_000,
        refsTokens: 5_000,
        systemContextTokens: 22_000,
        scaffoldChars: 24_000,
        refsChars: 16_000,
        systemContextChars: 70_000,
      };
    case "heavy":
      return {
        scaffoldTokens: 32_000,
        refsTokens: 16_000,
        systemContextTokens: 80_000,
        scaffoldChars: 60_000,
        refsChars: 50_000,
        systemContextChars: 256_000,
      };
    default:
      return {
        scaffoldTokens: 22_000,
        refsTokens: 12_000,
        systemContextTokens: 60_000,
        scaffoldChars: 42_000,
        refsChars: 38_000,
        systemContextChars: 192_000,
      };
  }
}

function deriveReferenceCategories(
  resolvedScaffold: ScaffoldManifest | null,
  routePlan: RoutePlan,
  preGenerationContracts: PreGenerationContractContext,
): string[] {
  const categories = new Set<string>();

  switch (resolvedScaffold?.id) {
    case "base-nextjs":
      categories.add("starter");
      break;
    case "landing-page":
    case "content-site":
      categories.add("marketing-sites");
      break;
    case "saas-landing":
      categories.add("saas");
      categories.add("marketing-sites");
      break;
    case "portfolio":
      categories.add("portfolio");
      break;
    case "blog":
      categories.add("blog");
      break;
    case "dashboard":
    case "app-shell":
      categories.add("admin-dashboard");
      break;
    case "auth-pages":
      categories.add("authentication");
      break;
    case "ecommerce":
      categories.add("ecommerce");
      break;
    default:
      break;
  }

  if (routePlan.routes.some((route) => route.path.startsWith("/docs"))) {
    categories.add("documentation");
  }

  if (
    preGenerationContracts.contracts.integrations.length > 0 ||
    preGenerationContracts.contracts.dataMode === "persisted"
  ) {
    categories.add("backend");
  }

  return Array.from(categories);
}

function deriveForbiddenPatterns(params: {
  buildIntent: BuildIntent;
  generationMode: BuildSpecGenerationMode;
  changeScope: BuildSpecChangeScope;
  previewPolicy: BuildSpecPreviewPolicy;
}): string[] {
  const { buildIntent, generationMode, changeScope, previewPolicy } = params;
  const patterns = new Set<string>([
    "leave_bracket_placeholders",
    "compat_preview_primary",
  ]);

  if (buildIntent !== "app") {
    patterns.add("unrequested_app_shell");
  }
  if (generationMode === "followUp" && changeScope !== "redesign") {
    patterns.add("unrequested_full_redesign");
  }
  if (previewPolicy === "fidelity2") {
    patterns.add("require_full_build_verification");
  }
  if (changeScope === "copy") {
    patterns.add("layout_reset_for_copy_change");
  }

  return Array.from(patterns);
}

export function deriveBuildSpec(params: DeriveBuildSpecParams): BuildSpec {
  const {
    prompt,
    buildIntent,
    generationMode,
    resolvedScaffold,
    routePlan,
    preGenerationContracts,
    promptStrategyMeta = null,
    capabilities = null,
    isFirstCodeGeneration,
    existingShellRoutePaths,
    previewPolicyOverride,
  } = params;

  const capabilityHeavy = capabilities ? hasHeavyCapabilities(capabilities) : false;
  const routeRealization = deriveRouteRealizationPolicy({
    generationMode,
    buildIntent,
    prompt,
    routePlan,
    isFirstCodeGeneration,
    existingShellRoutePaths,
  });

  const changeScope = inferChangeScope({
    prompt,
    generationMode,
    routePlan,
    preGenerationContracts,
  });
  const previewPolicy = previewPolicyOverride ?? inferPreviewPolicy();
  const qualityTarget = inferQualityTarget({
    buildIntent,
    generationMode,
    resolvedScaffold,
    routePlan,
    routeRealization,
    preGenerationContracts,
    previewPolicy,
  });
  const verificationPolicy = inferVerificationPolicy({
    generationMode,
    changeScope,
    previewPolicy,
    capabilityHeavy,
  });
  const contextPolicy = inferContextPolicy({
    prompt,
    generationMode,
    changeScope,
    buildIntent,
    routePlan,
    routeRealization,
    preGenerationContracts,
    promptStrategyMeta,
    capabilityHeavy,
  });

  return {
    buildIntent,
    generationMode,
    changeScope,
    scaffoldId: resolvedScaffold?.id ?? null,
    routePlanSummary: buildRoutePlanSummary(routePlan),
    stylePack: inferStylePack(prompt, buildIntent, resolvedScaffold, changeScope),
    qualityTarget,
    previewPolicy,
    verificationPolicy,
    contextPolicy,
    referenceCategories: deriveReferenceCategories(
      resolvedScaffold,
      routePlan,
      preGenerationContracts,
    ),
    forbiddenPatterns: deriveForbiddenPatterns({
      buildIntent,
      generationMode,
      changeScope,
      previewPolicy,
    }),
    tokenBudgets: tokenBudgetsForContextPolicy(contextPolicy),
    routeRealization,
  };
}
